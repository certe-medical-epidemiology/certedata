msg <- function(..., startup = FALSE) {
  if (startup) {
    if (!isTRUE(getOption("certedata.quiet"))) {
      packageStartupMessage(text_col(...))
    }
  } else {
    message(text_col(...))
  }
}

text_col <- function(x) {
  # If RStudio not available, messages already printed in black
  if (!rstudioapi::isAvailable()) {
    return(x)
  }

  if (!rstudioapi::hasFun("getThemeInfo")) {
    return(x)
  }

  theme <- rstudioapi::getThemeInfo()
  if (isTRUE(theme$dark)) crayon::white(x) else crayon::black(x)

}

#' List all packages in the certedata
#'
#' @param include_self Include certedata in the list?
#' @export
#' @examples
#' certedata_packages()
certedata_packages <- function(include_self = TRUE) {
  names <- core_available
  if (include_self) {
    names <- c(names, "certedata")
  }
  names
}

invert <- function(x) {
  if (length(x) == 0) return()
  stacked <- utils::stack(x)
  tapply(as.character(stacked$ind), stacked$values, list)
}

style_grey <- function(level, ...) {
  crayon::style(
    paste0(...),
    crayon::make_style(grDevices::grey(level), grey = TRUE)
  )
}

is_attached <- function(x) {
  paste0("package:", x) %in% search()
}
